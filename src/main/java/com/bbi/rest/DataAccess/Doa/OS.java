package com.bbi.rest.DataAccess.Doa;

public class OS {

	private int OSId;
	private String OSName;
	private String OSExtra_OS;
	private int OSManager;

	public int getOSId() {
		return OSId;
	}

	public void setOSId(int oSId) {
		OSId = oSId;
	}

	public String getOSName() {
		return OSName;
	}

	public void setOSName(String oSName) {
		OSName = oSName;
	}

	public String getOSExtra_OS() {
		return OSExtra_OS;
	}

	public void setOSExtra_OS(String oSExtra_OS) {
		OSExtra_OS = oSExtra_OS;
	}

	public int getOSManager() {
		return OSManager;
	}

	public void setOSManager(int oSManager) {
		OSManager = oSManager;
	}

}